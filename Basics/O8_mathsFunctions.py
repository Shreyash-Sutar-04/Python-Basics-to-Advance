"""
1) round() :- gives the Round figure of the example 
2) abs() :- Provides the absolute value of the value
3) pow() :- Power function usefull to find out the power of the value
4) max() :- Gives the maximum value by comparing
5) min():-  Give minimum value among values 
"""

x = 3.14
y = -4
z = 5

result = round(x)
result1 = abs(y)
result2 = pow(3,2) #means 3*3
result3 = max(x,y,z)
result4 = min(x,y,z)
print(result)
print(result1)
print(result2)
print(result3)
print(result4)